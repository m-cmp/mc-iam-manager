	e := echo.New()

	// Validator 설정
	e.Validator = &CustomValidator{validator: validator.New()}

	// 로그 레벨 설정
	e.Debug = true

	// 미들웨어 설정
	e.Use(echomiddleware.LoggerWithConfig(echomiddleware.LoggerConfig{
		Format: "[${time_rfc3339}] ${level} ${method} ${uri} ${status} ${latency_human}\n",
		Output: os.Stdout,
	}))
